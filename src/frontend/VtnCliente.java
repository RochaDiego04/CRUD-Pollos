    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        objCliente.setNombre(txt_nombre.getText());
        objCliente.setApellido(txt_apellido.getText());
        objCliente.setEmail(txt_email.getText());
        objCliente.setEdad(Integer.parseInt(txt_edad.getText()));
        objCliente.setNumDeTelefono(txt_telefono.getText());
        objCliente.setNumDeCliente(txt_numDeCliente.getText());
        if(radbtn_masculino.isSelected()){
            objCliente.setSexo("Masculino");
        }
        else{
            objCliente.setSexo("Femenino");
        }
        
        objBd.abrir_conexion();
        objBd.insertar_cliente(objCliente);
        objBd.cerrar_conexion();
        
        JOptionPane.showMessageDialog(this, "Datos almacenados");
        limpiar_cajas();
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_mostrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrarClienteActionPerformed
        
        objBd.abrir_conexion();
        objCliente = objBd.consultar_cliente(txt_ingresoNumDeCliente.getText());
        objBd.cerrar_conexion();
        
        if(objCliente != null){
            txt_datosCliente.setForeground(Color.black);
            txt_datosCliente.setText(objCliente.mostrar_datos());
        }else{
            txt_ingresoNumDeCliente.setText("Ingresa aquí el numero de cliente");
            txt_ingresoNumDeCliente.setForeground(new Color(153,153,153));
            txt_datosCliente.setText("Aquí aparecerán los datos de tu cliente...");
            txt_datosCliente.setForeground(new Color(153,153,153));
            JOptionPane.showMessageDialog(this, "Cliente no fue encontrado en la base de datos")                                                                 ;
        }
        
        txt_datosCliente.setText(objCliente.mostrar_datos());
        
                                           
        
    }//GEN-LAST:event_btn_mostrarClienteActionPerformed

