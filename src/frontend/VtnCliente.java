    private void btn_buscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarClienteActionPerformed
        objBd.abrir_conexion();
        objCliente = objBd.consultar_cliente(txt_numClienteAActualizar.getText());
        objBd.cerrar_conexion();
        
        
        if(objCliente!=null) {
            JOptionPane.showMessageDialog(this, "Cliente encontrado, lo puedes actualizar");
            habilitar_cajas();
            txt_nombreActualizar.setText(objCliente.getNombre());
            txt_apellidoActualizar.setText(objCliente.getApellido());
            txt_emailActualizar.setText(objCliente.getEmail());
            txt_edadActualizar.setText(Integer.toString(objCliente.getEdad()));
            txt_telefonoActualizar.setText(objCliente.getNumDeTelefono());
            
            
        }else{
            JOptionPane.showMessageDialog(this, "Cliente no encontrado");
            deshabilitar_cajas();
            txt_numClienteAActualizar.setText("Ingresa aquí el numero de cliente");
            txt_numClienteAActualizar.setForeground(new Color(153,153,153));
        }  
    }//GEN-LAST:event_btn_buscarClienteActionPerformed

    private void btn_buscarCliente1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarCliente1ActionPerformed
        objBd.abrir_conexion();
        if(txt_tablaCliente.getText().isBlank() || "Ingresa el numero de cliente".equals(txt_tablaCliente.getText()))
            modeloCliente = objBd.consultar_cliente(modeloCliente);
        else
            modeloCliente = objBd.consultar_cliente(txt_tablaCliente.getText(), modeloCliente);
        
        if(modeloCliente.getRowCount()!= 0)
            tbl_Cliente.setModel(modeloCliente);
        else
            JOptionPane.showMessageDialog(this, "Cliente no se encuentra en la base de datos");
        
        objBd.cerrar_conexion();
        limpiar_cajas();
    }//GEN-LAST:event_btn_buscarCliente1ActionPerformed

